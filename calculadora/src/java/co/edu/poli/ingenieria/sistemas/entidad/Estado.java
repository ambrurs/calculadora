public class Estado {
    
   public String labelEstado ;
   public Integer idEstado;
   public String estiloEstado;

    public String getLabelEstado() {
        return labelEstado;
    }

    public void setLabelEstado(String labelEstado) {
        this.labelEstado = labelEstado;
    }

    public Integer getIdEstado() {
        return idEstado;
    }

    public void setIdEstado(Integer idEstado) {
        this.idEstado = idEstado;
    }

    public String getEstiloEstado() {
        return estiloEstado;
    }

    public void setEstiloEstado(String estiloEstado) {
        this.estiloEstado = estiloEstado;
    }
    
    
    
}
